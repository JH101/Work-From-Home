person = input("Please enter the person who said the qoute: " )

quote = input("Please enter the quote: ")

quote_upper_case = quote.upper()

print("{0} -{1}".format(quote_upper_case, person))

quote_lower_case = quote.lower()

print("{0} -{1}".format(quote_lower_case, person))

quote_capitalize = quote.capitalize()

print("{0} -{1}".format(quote_capitalize, person))

quote_title = quote.title()

print("{0} -{1}".format(quote_title, person))







